# operadores aritmeticos 

print ("Suma: ", 5 + 3)
print ("Resta: ", 5 - 3)
print ("Multiplicacion: ", 5 * 3)
print ("Division: ", 20 / 3)
print ("Division entera: ", 20 // 3) 
print ("Modulo: ", 20 % 3)
print ("Potencia: ", 5 ** 3)    

# operadores en cadenas de texto
print ("Hola " + "Mundo") #concatenacion
print ("Hola " * 3) # repeticion
print ("Hola " + "Mundo" * 3) # concatenacion con repeticion

# operadores de comparacion
print ("5 es igual a 3: ", 5 == 3) # compara si son iguales
print ("5 es diferente a 3: ", 5 != 3) # compara si son diferentes
print ("5 es mayor que 3: ", 5 > 3) # compara si es mayor
print ("5 es menor que 3: ", 5 < 3) # compara si es menor
print ("5 es mayor o igual que 3: ", 5 >= 3) # compara si es mayor o igual
print ("5 es menor o igual que 3: ", 5 <= 3) # compara si es menor o igual

# operadores de existencia
print ("ola" in "Hola") #verifica existencia de la cadena
print ("ola" not in "Hola") #verifica que no exista en la cadena

# operadores logicos - booleanos
print ("And: ", True and False) #and verifica si ambas condiciones son verdaderas
print ("Or: ", True or False) #or verifica si al menos una condicion es verdadera
print ("Not: ", not True) #not invierte el valor de la condicion

